#Faça um programa que leia um nº inteiro e mostre sua tabuada

print("Programa de Tabuada")
print("-=====================-")

numero = int(input("Digite um número para gerar a tabuada\n"))
print(f"O número digitado foi {numero}\nSua tabuada é: ")
print(f">> {numero*1}\n>> {numero*2}\n>> {numero*3}\n>> {numero*4}\n>> {numero*5}\n>> {numero*6}\n>> {numero*7}\n>> {numero*8}\n>> {numero*9}")